!##############################################################################
!#
!# @模块名称: fitting_module (参数配置模块)
!#
!# @功能描述: 
!#   本模块存储所有全局参数和配置数据，类似于Python中的配置类(Config)。
!#   包含衍射数据、晶体参数、约束条件等全局变量。
!#
!# @使用说明:
!#   use fitting_module
!#   访问全局变量进行读取和修改
!#
!##############################################################################

module fitting_module
    implicit none
    
    !======================================================================
    ! 基础数据数组大小参数
    !======================================================================
    integer, parameter :: maxdata = 1000  ! 最大数据点数
    
    !======================================================================
    ! 衍射实验数据数组
    !======================================================================
    real*8 :: x(maxdata)                   ! 位置数据（备用）
    real*8 :: value(maxdata)               ! 观测值 (phi角或y1值，根据level选择)
    real*8 :: value1(maxdata)              ! 观测q值
    
    !======================================================================
    ! 权重和校准参数
    !======================================================================
    real*8 :: e2, e3, e4                   ! 误差权重参数
    real*8 :: wavelength                   ! X射线波长
    real*8 :: x1                           ! 检测器参数
    real*8 :: sym_e                        ! 对称性权重因子
    real*8 :: sym_cal                      ! 计算对称性因子
    
    !======================================================================
    ! Miller指数转换矩阵
    !======================================================================
    real*8 :: Miller_trans(maxdata, 3)     ! 每个数据点对应的Miller指数 (h,k,l)
    
    !======================================================================
    ! 状态标志和模式控制
    !======================================================================
    integer :: sym_stat                    ! 对称性状态标志 (0=无, 1=有)
    integer :: level                       ! 计算模式: 1=q-psi模式, 2=theta模式
    integer :: tilt_check                  ! tilt优化标志: 0=不优化, 1=优化
    integer :: crystal_system              ! 晶体系统标志: 0=无限制, 1=正交
    
    !======================================================================
    ! 固定晶面功能
    !======================================================================
    integer :: fixhklfile                  ! 固定晶面文件数量 (0=无固定)
    integer, allocatable :: fixhkl(:,:)    ! 固定晶面列表: (编号, h, k, l)
    
    !======================================================================
    ! a*与b*垂直约束
    !======================================================================
    integer :: ortho_ab_star               ! a*与b*垂直约束标志: 0=无, 1=启用
    
    !======================================================================
    ! Miller指数限制相关变量
    !======================================================================
    integer :: max_h1_in, max_k1_in, max_l1_in  ! 用户输入的Miller指数限制
    integer :: max_h1, max_k1, max_l1            ! 实际使用的Miller指数限制
    integer :: h_user_set, k_user_set, l_user_set  ! 用户设置标志
    real*8 :: max_q                              ! 最大q值（从衍射数据计算）
    integer :: max_h1_by_cell, max_k1_by_cell, max_l1_by_cell  ! 基于晶胞参数的Miller指数
    integer :: max_h1_by_q, max_k1_by_q, max_l1_by_q  ! 基于q值的Miller指数
    
    !======================================================================
    ! 晶胞参数边界限制
    !======================================================================
    real*8 :: max_values(6), min_values(6)  ! 参数上下限 [a,b,c,alpha,beta,gamma]
    
    !======================================================================
    ! 数据贡献权重
    !======================================================================
    real*8 :: contribution(maxdata)         ! 每个数据点的权重贡献
    
    !======================================================================
    ! 晶胞参数初始值（用于输入文件读取）
    !======================================================================
    integer :: num_cell                     ! 晶胞参数组数
    real*8 :: amin, bmin, cmin, alphamin, betamin, gammamin  ! 参数下限
    real*8 :: amax, bmax, cmax, alphamax, betamax, gammamax  ! 参数上限
    
end module fitting_module
